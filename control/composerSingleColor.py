def line_delay(fd, time_to_wait):
  fd.write(chr(DELAYLINE))
  fd.write(chr(time_to_wait))
  fd.write('\n')
  return;

def line_turnoff(fd):
  fd.write(chr(TURNOFFLINE))
  fd.write('\n')
  return;

